package com.everis.academia.java.agenda.digital.entidades;

public class Cidade {

	private Integer codigo;
	private String nome;

	public Cidade() {
		super();
	}

	public Integer getCodigo() {
		return codigo;
	}

	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
}
